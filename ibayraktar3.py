#Calculating volume of a cylinder

radius=input("The radius of the cylinder is ")
r= int(radius)

height=input("The height of the cylinder is ")
h=int(height)
pi=3.14
volume=r*r*h*pi
print("The volume of the cylinder is ", (volume))
print ("You wonder how I came up with this number!") 
print ("What do you expect, you entered the radius as ", (r), "and the height as ", (h))
print ("Don't forget the pi too!, The volume of the cylinder is, ", (r), "*", (r), "*", (h), "*", (pi))

